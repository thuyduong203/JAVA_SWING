    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        JOptionPane.showMessageDialog(this, sanPhamServiceImpl.themSP(newSanPham()));
        listSP = sanPhamServiceImpl.getAllSP();
        showData(listSP);
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int index = tblSanPham.getSelectedRow();
        if (index < 0) {
            JOptionPane.showMessageDialog(this, "Chọn dòng cần sửa!");
        } else {
            String idSP = listSP.get(index).getIdSP();
            SanPham sanPham = newSanPham();
            JOptionPane.showMessageDialog(this, sanPhamServiceImpl.updateSP(sanPham, idSP));
            listSP = sanPhamServiceImpl.getAllSP();
            showData(listSP);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnClearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClearActionPerformed
        txtMaSP.setText("");
        txtTenSP.setText("");
    }//GEN-LAST:event_btnClearActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int index = tblSanPham.getSelectedRow();
        if (index < 0) {
            JOptionPane.showMessageDialog(this, "Chọn dòng cần xoá!");
        } else {
            String idSP = listSP.get(index).getIdSP();
            String deleteHDCT = hoaDonChiTietImpl.deleteHdctBySanPham(idSP);
            String deleteGHCT = gioHangChiTietImpl.deleteGHCTBySanPham(idSP);
            String deleteCTSP = chiTietSPServiceImpl.deleteCTSPByIdSP(idSP);
            JOptionPane.showMessageDialog(this, sanPhamServiceImpl.deleteSP(idSP));
            listSP = sanPhamServiceImpl.getAllSP();
            showData(listSP);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

